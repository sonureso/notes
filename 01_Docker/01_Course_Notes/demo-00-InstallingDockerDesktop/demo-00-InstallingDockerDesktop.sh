# Install Docker on MacOS


# install docker desktop using following link
https://docs.docker.com/

# Click on "Get Docker"

# Show the 3 choices we can use to run Docker Desktop

# Click on Docker Desktop for Mac


# Click on mac Intel chip

# Our mac processor is Intel core, so we will use Intel chip
# After that click on the dmg file and install it

# Then copy docker to applications

# Open docker from applications folder

# You can show the top bar also while docker is executing

# Open the Docker Desktop (don't do anything now)

# Switch over to the terminal window (start in the ~/docker-demos/ folder)
# We will run all our demos in this folder

# Run

docker version


